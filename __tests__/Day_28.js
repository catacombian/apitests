const URL = 'https://www.postman.com/';
const frisby = require('frisby');

jest.setTimeout(15000);
it('Lighthouse',  () => {
  frisby.get(
    `https://www.googleapis.com/pagespeedonline/v5/runPagespeed?url=${URL}`
  )
  .expect('status', 200)
  .expect('json', {
    data: {
      serverResponseTime: '46',
    }
  });
});